/*
print any message in sequence of 1 to 10.
 */
public class WhileLoop
{
    public static void main(String[] args)
    {
        int number;   //declaretion
        number = 1;   //initilization
        while(1 <= 10)  //condition
        {
            System.out.println(number + ".hey there!");
            number ++;
        }

    }
}